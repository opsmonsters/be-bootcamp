
class ArithmeticCompound {

    public static void main(String[] args) {
        // shorthand for addition
        int a = 20;
        a += 15; // this is same as a = a + 15
        System.out.println(a);

        // short hand for subtraction
        int b = 20;
        b -= 15; // this is same as b = b - 15
        System.out.println(b);

        // short hand for multiplication
        int c = 20;
        c *= 2; // this is same as c = c * 2
        System.out.println(c);

        int d = 20;
        d /= 2; //this is same as d = d / 2
        System.out.println(d);

    }
}
