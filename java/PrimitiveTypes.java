
class PrimitveTypes {

    public static void main(String[] args) {
        byte age = 20;
        short numberOfDaysInYear = 265;
        int registerNumber = 8_39_48_293;
        long viewCount = 1_00_00_000L;

        System.out.println(age);
        System.out.println(numberOfDaysInYear);
        System.out.println(registerNumber);
        System.out.println(viewCount);

        float runRate = 6.24F;
        double pi = 3.414;

        System.out.println(runRate);
        System.out.println(pi);

        char grade = 'A';
        System.out.println(grade);

        boolean isTheSkyBlue = true;
        System.out.println(isTheSkyBlue);
    }

}
